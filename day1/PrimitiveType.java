class PrimitiveType{
    public static void main(String[] args) {
        byte  b = -128;
        short s = 1234;
        int   i = 123456789;
        long  l = 1234567891;
        float f = 12.1020f;
        double  d = 125678.10001d;
        char  c = 'a';
        boolean bo = true; 
        System.out.println(" -- Primitive Data Types -- ");
        System.out.println("Byte : " + (b) + "\nShort : " + s + "\nInt : " + i + "\nLong : " + l +
            "\nFloat : " + f + "\nDouble : " + d + "\nChar : " + c + "\nBoolean : " + bo);
    }
}