//LOGICAL SWAPPPPP (sexyyyy swap)
class SexySWAP{
    public static void main(String[] args){
        int a = 5, b = 10;
        System.out.println("Before : \n" + "a : " + a + "\nb : " + b);
        System.out.println("SWAPPING....");
        a = a+b;
        b = a-b;
        a = a-b;
        System.out.println("After  : \n" + "a : " + a + "\nb : " + b);
    }
}